# 3. Estimation Methods
# =============================================================================
#
# An ARMA model requires the time series to be stationary. Before estimation,
# check stationarity (e.g., ADF test or KPSS test).
#
# The order (p, q) is unknown and needs to be determined:
#   - Using ACF and PACF plots
#   - Using information criteria such as AIC, BIC, etc.
#
# Note: ARIMA = AutoRegressive Integrated Moving Average. We build an
# ARIMA(p, d, q) model by building an ARMA(p, q) model on the differenced
# series.
#
# Common parameter estimation approaches:
#   - Innovations algorithm
#   - Method of moments
#   - Conditional least squares
#   - Maximum likelihood
# (See section 4.2 in "Applied Time Series Analysis and Forecasting with Python")


# =============================================================================
# 4. Forecasting
# =============================================================================
#
# Let {X_{1:n}} be the known data used to forecast future values beyond the
# present n (the forecast origin). The forecast of X_{n+h} is X_hat_n(h).
# The forecast error is e_n(h) = X_{n+h} - X_hat_n(h).
#
# We use the minimum mean squared error criterion:
#
#     E[e_n(h)^2] = E[(X_{n+h} - X_hat_n(h))^2]
#                 = min_g { E[X_{n+h} - g(X_{1:n})]^2 }
#
# Key results:
#
# 1. X_hat_{n+1} = E(X_{n+1} | X_1, ..., X_n)  for n >= 1.
#    Thus X_hat_n(h) can be recursively computed with the innovations algorithm.
#
# 2. There exist coefficients {phi_{nj}; 1 <= j <= n} such that
#    X_hat_{n+1} = phi_{n1} X_n + ... + phi_{nn} X_1
#    (the best linear predictor for X_{n+1}).
#
# 3. For a causal ARMA(p, q) model with mean zero, one-step-ahead predictors:
#
#    X_hat_{n+1} =
#      0,                                                    if n = 0
#      sum_{j=1}^{n} theta_{nj} (X_{n+1-j} - X_hat_{n+1-j}),
#                                                    if 1 <= n < max(p,q)
#      sum_{i=1}^{p} phi_i X_{n+1-i}
#        + sum_{j=1}^{q} theta_{nj} (X_{n+1-j} - X_hat_{n+1-j}),
#                                                    if n >= max(p,q)
#
#    where theta_{nj} are from the innovations algorithm.
#
# 4. If n > max(p, q), for all h >= 1, the h-step-ahead predictor is:
#
#    X_hat_n(h) = sum_{i=1}^{p} phi_i X_hat_n(h-i)
#                 + sum_{j=h}^{q} theta_{n+h-1,j} (X_{n+h-j} - X_hat_{n+h-j})
#
#    where X_hat_{n+h-j} is a one-step-ahead predictor.
